    private BigDecimal price;
    @Size(max = 200)
    @Column(name = "image")
    private String image;
    @OneToMany(mappedBy = "product")
    private Collection<OrderDetail> orderDetailCollection;

    public Product() {
    }

    public Product(Integer id) {
        this.id = id;
    }

